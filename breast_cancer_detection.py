#Initializing Weight and bias
def Weightbias(dimension):
    w = np.random.randn(dimension, 1) * 0.01
    b = 0.0
    return w, b

#Sigmoid() squashes the input value z between 0 and 1 making it suitable for binary classification.
def sigmoid(z):
    return 1 / (1 + np.exp(-z))

#Forward-Backward Propagation
def forward_backward_propagation(w, b, x_train, y_train):
    m = x_train.shape[1]
    z = np.dot(w.T, x_train) + b
    y_head = sigmoid(z)


    cost = (-1/m) * np.sum(y_train * np.log(y_head) + (1 - y_train) * np.log(1 - y_head))

    derivative_weight = (1/m) * np.dot(x_train, (y_head - y_train).T)
    derivative_bias = (1/m) * np.sum(y_head - y_train)

    gradients = {"Derivative Weight": derivative_weight, "Derivative Bias": derivative_bias}
    return cost, gradients

#Updating parameters
def update(w, b, x_train, y_train, learning_rate, num_iterations):
    costs = []

    for i in range(num_iterations):
        cost, grad = forward_backward_propagation(w, b, x_train, y_train)

        w -= learning_rate * grad["Derivative Weight"]
        b -= learning_rate * grad["Derivative Bias"]

        if i % 100 == 0:
            costs.append(cost)
            print(f"Cost after iteration {i}: {cost}")

    parameters = {
        "weight": w,
        "bias": b
    }

    return parameters, grad, costs

def predict(w, b, x_test):
    m = x_test.shape[1]
    y_pred = np.zeros((1, m))
    z = sigmoid(np.dot(w.T, x_test) + b)

    for i in range(z.shape[1]):
        y_pred[0, i] = 1 if z[0, i] > 0.5 else 0

    return y_pred

#logistic regression
def logistic_regression(x_train, y_train, x_test, y_test, learning_rate=0.01, num_iterations=1000):
    dimension = x_train.shape[0]
    w, b = Weightbias(dimension)
    parameters, gradients, costs = update(w, b, x_train, y_train, learning_rate, num_iterations)

    y_prediction_test = predict(parameters["weight"], parameters["bias"], x_test)
    y_prediction_train = predict(parameters["weight"], parameters["bias"], x_train)

    print(f"Train accuracy: {100 - np.mean(np.abs(y_prediction_train - y_train)) * 100}%")
    print(f"Test accuracy: {100 - np.mean(np.abs(y_prediction_test - y_test)) * 100}%")

logistic_regression(x_train, y_train, x_test, y_test, learning_rate=0.01, num_iterations=1000) #runs the logistic regression model with the given training and test data