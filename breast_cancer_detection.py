# Dropping 'id' and 'Unnamed: 32' as they have no role in prediction
df.drop(['Unnamed: 32', 'id'], axis=1, inplace=True)
df['diagnosis'] = df['diagnosis'].map({'M': 1, 'B': 0}) #Converts the diagnosis column, which contains 'M' (Malignant) and 'B' (Benign) into binary values.

y = df['diagnosis'].values
data = df.drop(['diagnosis'], axis=1)

x = (data - data.min()) / (data.max() - data.min()) #normalization of data

x_train, x_test, y_train, y_test = train_test_split(x, y, test_size = 0.15, random_state = 42) #15% of the data will be used for testing and the rest for training

#Transpose(T) to ensure that the data has the correct shape for matrix operations during the logistic regression.
x_train = x_train.T
x_test = x_test.T
y_train = y_train.T
y_test = y_test.T

print("x train: ", x_train.shape)
print("x test: ", x_test.shape)
print("y train: ", y_train.shape)
